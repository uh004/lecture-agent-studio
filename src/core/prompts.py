def extract_openers(scripts: list) -> list:
    """이전 스크립트들에서 첫 문장(시작 표현)을 추출"""
    openers = []
    for s in scripts:
        first_sent = s.strip().split(".")[0] if s else ""
        if first_sent and len(first_sent) > 5:
            openers.append(first_sent.strip())
    return openers


def count_word(text: str, word: str) -> int:
    return text.count(word)


def limit_word(text: str, word: str, max_count: int = 2) -> str:
    """특정 단어가 max_count회를 초과하면 초과분을 제거"""
    count = 0
    result = []
    for sent in re.split(r'(?<=[.!?])\s+', text):
        if word in sent:
            count += 1
            if count > max_count:
                sent = sent.replace(word, "").strip()
                if len(sent) < 5:
                    continue
        result.append(sent)
    return " ".join(result)


